class keluarga:
  def __init__(self, bapak, ibu, kakak):
      self.bapak = bapak
      self.ibu = ibu
      self.kakak = kakak
      
nama = keluarga('Muh','Nur','Wahyu Andriawan')
#Untuk Mengakses class diatas caranya sebagai berikut:
print(nama.bapak)
print(nama.ibu)
print(nama.kakak)


